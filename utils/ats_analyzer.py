"""
ATS analysis using Google Gemini.
Returns a structured dict with scores, keywords, and suggestions.
"""
import json
import os
import re

from google import genai


def _get_client():
    api_key = os.environ.get("GEMINI_API_KEY", "")
    if not api_key:
        raise EnvironmentError(
            "GEMINI_API_KEY is not set. Add it to your .env file or Streamlit secrets."
        )
    return genai.Client(api_key=api_key)


ANALYSIS_PROMPT = """
You are an expert ATS (Applicant Tracking System) resume analyzer.

Analyze the following resume against the provided job description for the role of "{job_role}".

Return ONLY a valid JSON object (no markdown, no explanation) with this exact structure:
{{
  "overall_score": <integer 0-100>,
  "section_scores": {{
    "keywords_match": <integer 0-100>,
    "skills_alignment": <integer 0-100>,
    "experience_match": <integer 0-100>,
    "education_match": <integer 0-100>,
    "format_structure": <integer 0-100>
  }},
  "matched_keywords": [<list of strings>],
  "missing_keywords": [<list of strings>],
  "strengths": [<list of 3-5 strength strings>],
  "improvements": [<list of 5-8 specific, actionable improvement strings>],
  "summary": "<2-3 sentence executive summary of the match>"
}}

---
JOB ROLE: {job_role}

JOB DESCRIPTION:
{job_description}

---
RESUME:
{resume_text}
"""


def analyze_resume(job_role: str, job_description: str, resume_text: str) -> dict:
    """
    Call Gemini to analyze a resume against a job description.
    Returns a parsed dict with all ATS metrics.
    """
    client = _get_client()

    prompt = ANALYSIS_PROMPT.format(
        job_role=job_role,
        job_description=job_description,
        resume_text=resume_text,
    )

    # Try models in order — use what's actually available for this API key
    # (gemini-1.5-* is NOT available; confirmed via client.models.list())
    models_to_try = [
        "gemini-2.0-flash-lite",   # lightest, best for free quota
        "gemini-2.0-flash",        # standard fallback
        "gemini-2.5-flash",        # more capable fallback
    ]

    last_error = None
    for model_name in models_to_try:
        try:
            response = client.models.generate_content(
                model=model_name,
                contents=prompt,
            )
            raw = response.text.strip()
            break
        except Exception as e:
            last_error = e
            if "429" in str(e) or "RESOURCE_EXHAUSTED" in str(e):
                continue   # try next model
            raise          # non-quota error → surface immediately
    else:
        raise RuntimeError(
            f"All Gemini models are rate-limited. Please wait a minute and try again.\n"
            f"Last error: {last_error}"
        )

    # Strip markdown code fences if Gemini wraps the JSON
    raw = re.sub(r"^```(?:json)?\s*", "", raw)
    raw = re.sub(r"\s*```$", "", raw)

    try:
        result = json.loads(raw)
    except json.JSONDecodeError as e:
        raise ValueError(
            f"Gemini returned invalid JSON:\n{raw}\n\nError: {e}"
        ) from e

    # Validate required keys
    required_keys = [
        "overall_score",
        "section_scores",
        "matched_keywords",
        "missing_keywords",
        "strengths",
        "improvements",
        "summary",
    ]
    for key in required_keys:
        if key not in result:
            raise ValueError(f"Missing key in Gemini response: {key}")

    return result


def generate_text_report(
    job_role: str, job_description: str, resume_text: str, analysis: dict
) -> str:
    """Generate a plain-text downloadable report."""
    lines = [
        "=" * 60,
        "ATS RESUME ANALYSIS REPORT",
        "=" * 60,
        f"Job Role: {job_role}",
        f"Overall ATS Score: {analysis['overall_score']}/100",
        "",
        "SECTION SCORES",
        "-" * 40,
    ]
    for section, score in analysis["section_scores"].items():
        label = section.replace("_", " ").title()
        lines.append(f"  {label}: {score}/100")

    lines += [
        "",
        "EXECUTIVE SUMMARY",
        "-" * 40,
        analysis.get("summary", ""),
        "",
        "STRENGTHS",
        "-" * 40,
    ]
    for i, s in enumerate(analysis.get("strengths", []), 1):
        lines.append(f"  {i}. {s}")

    lines += [
        "",
        "MATCHED KEYWORDS",
        "-" * 40,
        ", ".join(analysis.get("matched_keywords", [])),
        "",
        "MISSING KEYWORDS",
        "-" * 40,
        ", ".join(analysis.get("missing_keywords", [])),
        "",
        "IMPROVEMENT SUGGESTIONS",
        "-" * 40,
    ]
    for i, tip in enumerate(analysis.get("improvements", []), 1):
        lines.append(f"  {i}. {tip}")

    lines += ["", "=" * 60, "Generated by ATS Resume Checker", "=" * 60]
    return "\n".join(lines)


