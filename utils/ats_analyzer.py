        text = re.sub(r"\*{1,2}([^*]+)\*{1,2}", r"\1", text)  # **bold** → bold
        text = re.sub(r"_{1,2}([^_]+)_{1,2}", r"\1", text)    # __x__ → x
        text = re.sub(r"`([^`]+)`", r"\1", text)               # `code` → code
